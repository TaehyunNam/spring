package chap07.user;

public class SchoolVo {

	private int schoolno;
	private int userno;
	private String school;
	private String year;
	
	int getSchoolno() {
		return schoolno;
	}
	void setSchoolno(int schoolno) {
		this.schoolno = schoolno;
	}
	int getUserno() {
		return userno;
	}
	void setUserno(int userno) {
		this.userno = userno;
	}
	String getSchool() {
		return school;
	}
	void setSchool(String school) {
		this.school = school;
	}
	String getYear() {
		return year;
	}
	void setYear(String year) {
		this.year = year;
	}
}
